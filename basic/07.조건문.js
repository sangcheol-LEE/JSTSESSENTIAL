// if 문 switch문

let age = 10;

if (age === 10) {
  console.log("나이는 10살입니다.");
} else {
  console.log("모르겠습니다.");
}

switch (age) {
  case 1:
    console.log(1);
  case 2:
    console.log(2);
  case 3:
    console.log(3);
  case 4:
    console.log(4);
  default:
    console.log("??");
    break;
}
